    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        
        if(Cek_Kode() == false){
            return;
        }
         try{
            String SQL;
            Connection Conn;
            Statement Stmt;
            ResultSet RS;
            Conn = (Connection) DriverManager.getConnection(Db_Koneksi,
                    Db_Username,Db_Password);
            String Kode_Pelanggan = kde.getText();
            String Nama_Pelanggan = nama.getText();
            String Jenis_Kelamin;
            if(pria.isSelected()){
                Jenis_Kelamin = "1";
            }else{
                Jenis_Kelamin = "0";
            }
            String Alamat = alamat.getText();
            String Telepon = tlp.getText();
            String Status;
            if(status.getSelectedItem() == "Sudah Menikah"){
                Status = "1";
            }else if(status.getSelectedItem() == "Belum Menikah"){
                Status = "2";
            }else{
                Status = "0";
            }
            Calendar Cal = Calendar.getInstance();
            SimpleDateFormat SDF = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String TanggalJam = SDF.format(Cal.getTime());
            SQL = "SELECT * FROM pelanggan WHERE kdpel = '" + Kode_Pelanggan + "'";        
            Stmt = Conn.createStatement();
            RS = Stmt.executeQuery(SQL);
            RS.close();
            SQL = "INSERT INTO pelanggan (";
            SQL += "kdpel,";
            SQL += "pel_nama,";
            SQL += "pel_jk,";
            SQL += "pel_alamat,";
            SQL += "pel_telp,";
            SQL += "pel_kawin,";
            SQL += "pel_create,";
            SQL += "pel_update";
            SQL += ") VALUES (";
            SQL += "'" + Kode_Pelanggan + "',";
            SQL += "'" + Nama_Pelanggan + "',";
            SQL += "'" + Jenis_Kelamin + "',";
            SQL += "'" + Alamat + "',";
            SQL += "'" + Telepon + "',";
            SQL += "'" + Status + "',";
            SQL += "'" + TanggalJam + "',";
            SQL += "'" + TanggalJam + "'";
            SQL += ")";
            Stmt = Conn.createStatement();
            Stmt.executeUpdate(SQL);
            Stmt.close();
            Conn.close();
            JOptionPane.showMessageDialog(rootPane,"Input Data Berhasil."
                        ,"Informasi",JOptionPane.INFORMATION_MESSAGE);
            kde.requestFocus();
        }catch (SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_saveActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        
        if(Cek_Kode() == false){
            return;
        }
        try{
            String Kode_Pelanggan = kde.getText();
            String Nama_Pelanggan = nama.getText();
            String Jenis_Kelamin;
            if(pria.isSelected()){
                Jenis_Kelamin = "1";
            }else{
                Jenis_Kelamin = "0";
            }
            String Alamat = alamat.getText();
            String Telepon = tlp.getText();
            String Status;
            if(status.getSelectedItem() == "Menikah"){
                Status = "1";
            }else if(status.getSelectedItem() == "Belum Menikah"){
                Status = "2";
            }else{
                Status = "0";
            }
            Calendar Cal = Calendar.getInstance();
            SimpleDateFormat SDF = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String TanggalJam = SDF.format(Cal.getTime());
            //==================SQL UPDATE=================//
            String SQL = "UPDATE pelanggan SET ";
            SQL += "pel_nama = '" + Nama_Pelanggan + "', ";
            SQL += "pel_jk = '" + Jenis_Kelamin + "', ";
            SQL += "pel_alamat = '" + Alamat + "', ";
            SQL += "pel_telp = '" + Telepon + "', ";
            SQL += "pel_kawin = '" + Status + "', ";
            SQL += "pel_update = '" + TanggalJam + "' ";
            SQL += "WHERE kdpel = '" + Kode_Pelanggan + "'";
            Connection Conn = (Connection) DriverManager.getConnection(Db_Koneksi,
                    Db_Username,Db_Password);
            Statement Stmt = Conn.createStatement();
            Stmt.executeUpdate(SQL);
            Stmt.close();
            Conn.close();
            JOptionPane.showMessageDialog(rootPane, "Data Berhasil diUpdate!",
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
            Bersih();
            kde.requestFocus();
        }catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        
        if(Cek_Kode() == false){
            return;
        }
        int Konfirmasi = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus?",
                "Konfirmasi",JOptionPane.YES_NO_OPTION);
        if(Konfirmasi == 0){
            try{
                String SQL = "DELETE FROM pelanggan WHERE kdpel = ?";
                Connection Conn = (Connection) DriverManager.getConnection(Db_Koneksi,
                    Db_Username,Db_Password);
                PreparedStatement pst = Conn.prepareStatement(SQL);
                pst.setString(1, kde.getText());
                pst.executeUpdate();
                Conn.close();
                JOptionPane.showMessageDialog(rootPane, "Data Berhasil diDelete!",
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
                Bersih();
                kde.requestFocus();
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

