    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        // TODO add your handling code here:
       
        if(Cek_Kode() == false){
            return;
        }
        try{
            String Kode_Pelanggan = kde.getText().trim();
            String SQL = "SELECT * FROM pelanggan WHERE kdpel = '" + Kode_Pelanggan + "'";
            Connection Conn = (Connection) DriverManager.getConnection(Db_Koneksi,
                    Db_Username,Db_Password);
            Statement Stmt = Conn.createStatement();
            ResultSet RS = Stmt.executeQuery(SQL);
            if(!RS.next()){
                JOptionPane.showMessageDialog(rootPane, "Kode Pelanggan tidak ditemukan!",
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
                kde.setText("");
                kde.requestFocus();
                return;
            }
            JOptionPane.showMessageDialog(rootPane, "Kode Pelanggan ditemukan!",
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
            nama.setText(RS.getString("pel_nama"));
            if(RS.getString("pel_jk").equals("1")){
                pria.setSelected(true);
            }else{
                wanita.setSelected(true);
            }
            alamat.setText(RS.getString("pel_alamat"));
            tlp.setText(RS.getString("pel_telp"));
            status.setSelectedItem(RS.getString("pel_kawin"));
            Stmt.close();
            Conn.close();
        }catch(SQLException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_cariActionPerformed

